package assignments;

public class As16 {

    public static void main(String[] args) {
        int marks = 75;

        if (marks >= 75)
        {
            System.out.println("Distinction class");
        } 
        else if (marks >= 60)
        {
            System.out.println("First class");
        } 
        else if (marks >= 35)
        {
            System.out.println("Second class");
        } 
        else {
            System.out.println("Fail");
        }
    }
}